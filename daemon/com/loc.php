function getmloc()
{
	global $reg,$m;
	$l=$m;
	foreach ($reg as $key => $mmas)
	{
		foreach ($mmas as $kkey => $mas)
		{
			foreach ($m as $k => $i)
			{
				if ($i==$mas)
				{
					$mloc[$key][$mas][]=$k;
					unset($l[$k]);
				}
			}
		}
	}
	return $mloc;
}
